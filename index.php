          }
        }
      };
      window.nextStep = function() {
        // On peut continuer si on a encore assez de questions
        if (allQuestions.length >= questionsPerStep) {
          startStep();
        } else {
          document.getElementById('quiz-container').innerHTML = `<h2>Quiz termin√© !</h2><p>F√©licitations, vous avez termin√© toutes les √©tapes !</p>`;
          showFireworks();
        }
        updateProgressBar();
      };
      window.retryStep = function() {
        startStep();
        updateProgressBar();
      };
      startStep();
      updateProgressBar();
    }
